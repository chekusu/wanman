#!/usr/bin/env node
/**
 * Build standalone `wanman` bundles.
 *
 * Steps:
 *   1. Ensure runtime & CLI are built (esbuild)
 *   2. Generate src/embedded-assets.ts with all runtime payloads
 *   3. esbuild → dist/wanman.mjs (single-file Node.js bundle)
 *   4. Optionally: bun build --compile → dist/wanman (native binary)
 *
 * Usage:
 *   node build-standalone.mjs              # JS bundle only (default)
 *   node build-standalone.mjs --compile    # JS bundle + macOS binary
 *   node build-standalone.mjs --linux      # JS bundle + Linux binary
 *   node build-standalone.mjs --all        # JS bundle + macOS + Linux binaries
 */

import * as fs from 'node:fs'
import * as path from 'node:path'
import { execSync } from 'node:child_process'

const cliRoot = path.dirname(new URL(import.meta.url).pathname)
const projectRoot = path.resolve(cliRoot, '../..')

// ── 1. Build runtime & CLI ──

console.log('[standalone] Building runtime & CLI...')
execSync('pnpm --filter @wanman/runtime build', { cwd: projectRoot, stdio: 'inherit' })
execSync('pnpm --filter @wanman/cli build', { cwd: projectRoot, stdio: 'inherit' })

// ── 2. Collect assets ──

function readAsset(relPath) {
  const full = path.join(projectRoot, relPath)
  if (!fs.existsSync(full)) return null
  return fs.readFileSync(full, 'utf-8')
}

function collectDir(dirPath, fileName) {
  const result = {}
  const full = path.join(projectRoot, dirPath)
  if (!fs.existsSync(full)) return result
  for (const entry of fs.readdirSync(full)) {
    const filePath = path.join(full, entry, fileName)
    if (fs.existsSync(filePath)) {
      result[entry] = fs.readFileSync(filePath, 'utf-8')
    }
  }
  return result
}

const entrypointJs = readAsset('packages/runtime/dist/entrypoint.js')
const cliJs = readAsset('packages/cli/dist/index.js')
const productsJson = readAsset('apps/container/products.json')

// Agent configs
const agentConfigs = {}
const containerDir = path.join(projectRoot, 'apps/container')
if (fs.existsSync(containerDir)) {
  for (const f of fs.readdirSync(containerDir)) {
    if (f.startsWith('agents') && f.endsWith('.json')) {
      agentConfigs[f] = fs.readFileSync(path.join(containerDir, f), 'utf-8')
    }
  }
}

// Agent guides (AGENT.md per agent)
const agentSkills = collectDir('packages/core/agents', 'AGENT.md')

// Shared skills (SKILL.md per skill)
const sharedSkills = collectDir('packages/core/skills', 'SKILL.md')

console.log(`[standalone] Collected assets:`)
console.log(`  entrypoint.js: ${(entrypointJs?.length / 1024).toFixed(0)} KB`)
console.log(`  cli index.js:  ${(cliJs?.length / 1024).toFixed(0)} KB`)
console.log(`  agent configs: ${Object.keys(agentConfigs).join(', ')}`)
console.log(`  agent skills:  ${Object.keys(agentSkills).join(', ')}`)
console.log(`  shared skills: ${Object.keys(sharedSkills).join(', ')}`)

// ── 3. Generate embedded-assets.ts ──

const escape = (s) => s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')

let code = `// Auto-generated by build-standalone.mjs — DO NOT EDIT\n\n`
code += `export const ENTRYPOINT_JS = \`${escape(entrypointJs)}\`\n\n`
code += `export const CLI_JS = \`${escape(cliJs)}\`\n\n`
code += `export const AGENT_CONFIGS: Record<string, string> = {\n`
for (const [name, content] of Object.entries(agentConfigs)) {
  code += `  '${name}': \`${escape(content)}\`,\n`
}
code += `}\n\n`
code += `export const AGENT_SKILLS: Record<string, string> = {\n`
for (const [name, content] of Object.entries(agentSkills)) {
  code += `  '${name}': \`${escape(content)}\`,\n`
}
code += `}\n\n`
code += `export const SHARED_SKILLS: Record<string, string> = {\n`
for (const [name, content] of Object.entries(sharedSkills)) {
  code += `  '${name}': \`${escape(content)}\`,\n`
}
code += `}\n\n`
code += productsJson
  ? `export const PRODUCTS_JSON: string | null = \`${escape(productsJson)}\`\n`
  : `export const PRODUCTS_JSON: string | null = null\n`

const assetsPath = path.join(cliRoot, 'src/embedded-assets.ts')
fs.writeFileSync(assetsPath, code)
console.log(`[standalone] Generated ${assetsPath} (${(code.length / 1024).toFixed(0)} KB)`)

// ── 4. Build JS bundle with esbuild (always) ──

const { build } = await import('esbuild')

await build({
  entryPoints: [path.join(cliRoot, 'src/index.ts')],
  bundle: true,
  platform: 'node',
  target: 'node20',
  format: 'esm',
  outfile: path.join(cliRoot, 'dist/wanman.mjs'),
  banner: { js: '#!/usr/bin/env node' },
  // Bundle everything — no externals (standalone includes run command)
})

const jsSize = fs.statSync(path.join(cliRoot, 'dist/wanman.mjs')).size
console.log(`[standalone] → dist/wanman.mjs (${(jsSize / 1024).toFixed(0)} KB)`)

// ── 5. Optionally compile with Bun ──

const args = process.argv.slice(2)
const wantCompile = args.includes('--compile') || args.includes('--linux') || args.includes('--all')
const buildLinux = args.includes('--linux') || args.includes('--all')
const buildDarwin = args.includes('--compile') || args.includes('--all')

if (wantCompile) {
  const bunBuild = (target, outfile) => {
    const targetFlag = target ? `--target=${target}` : ''
    const cmd = `bun build --compile ${targetFlag} src/index.ts --outfile ${outfile}`
    console.log(`[standalone] ${cmd}`)
    execSync(cmd, { cwd: cliRoot, stdio: 'inherit' })
    const stat = fs.statSync(path.join(cliRoot, outfile))
    console.log(`[standalone] → ${outfile} (${(stat.size / 1024 / 1024).toFixed(1)} MB)`)
  }

  if (buildDarwin) bunBuild(null, 'dist/wanman')
  if (buildLinux) bunBuild('bun-linux-x64', 'dist/wanman-linux')
}

// Cleanup generated file
fs.rmSync(assetsPath)
console.log('[standalone] Done!')
